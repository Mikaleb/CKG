<template>
    <div class="column" v-cloak>
        <template v-for="(item, index) in steps">
            <div class="flex">
                <div class="w-2/12">
                    <span> Etape {{index+1}}</span>
                </div>
                <div class="column">
                    <div class="flex">
                        <div class="w-11/12">
                        <textarea class="input_modal blck" type="text"
                                  @keyup.tab="addStep()" v-model="item.instruction" rows="6" name="step[]"
                                  id="step[]"></textarea>
                        </div>
                        <div class="w-3/12 is-flex-center" v-cloak v-if="index === (steps.length-1)">
                            <a @click="addStep()" class="button is-primary  is-small icon-delete">
                                <i class="fa fa-plus" aria-hidden="true"></i></a>
                        </div>
                        <div class="w-3/12  is-flex-center" v-cloak v-else-if="index === (steps.length-2)">
                            <a @click="removeStep(index)" class="button is-small icon-delete">
                                <i class="fa fa-minus" aria-hidden="true"></i></a>
                        </div>
                        <div class="column" v-cloak v-else="index === (item.length-1)">
                        </div>
                    </div>
                </div>
            </div>

        </template>

    </div>
</template>

<script>
    export default {
        props: {
            recipe_id: {
                type: Number,
                default: null,
            }
        },

        data: function () {
            return {
                counter: 0,
                steps: "",
            };
        },
        methods: {
            addStep() {
                this.counter++;
                this.steps.push({
                    name: '',
                    qtt: '',
                });
            },
            removeStep: function (index) {
                this.counter--;
                this.steps.splice(index, 1);
            },
            async getSteps() {
                axios.post('/api/recipe/get_steps/', {recipeid: this.recipe_id}).then(response => {
                    this.steps = response.data;
                });
            },
        },
        mounted() {
            this.getSteps();
        },
    }
</script>
